package ru.levelp.myzoo;

public class Animal {
    String name; // название животного
    String color; // цвет животного
    String kind; // хищник или травоядное
    String gender; // самка или самец

    Animal(String name, String color, String kind, String gender) {
        this.name = name;
        this.color = color;
        this.kind = kind;
        this.gender = gender;
    }
}


